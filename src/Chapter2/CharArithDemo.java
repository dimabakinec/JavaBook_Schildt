package Chapter2;

public class CharArithDemo {
    public static void main(String[] args) {

        char ch;
        ch = 'X';
        System.out.println("ch including  " + ch);
        ch++;
        System.out.println("now ch including " + ch);

        ch = 90;
        System.out.println("now ch including " + ch);


    }
}
